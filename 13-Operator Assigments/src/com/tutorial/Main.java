package com.tutorial;

public class Main {

    public static void main(String[] args){

        // Operator Assigment

        // Penjumlahan
        int a = 7;
        a += 6;
        System.out.printf("nilai a adalah %d \n",a);

        // Pengurangan
        int b = 18;
        b -= 5;
        System.out.printf("nilai b adalah %d \n",b);

        // Perkalian
        int c = 3;
        c *= 9;
        System.out.printf("nilai c adalah %d \n",c);

        // Pembagian
        int d = 150;
        d /= 30;
        System.out.printf("nilai d adalah %d \n",d);

        // Modulus
        int e = 12;
        e %= 7;
        System.out.printf("nilai e adalah %d",e);
    }

}
